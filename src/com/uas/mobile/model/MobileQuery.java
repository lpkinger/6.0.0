package com.uas.mobile.model;

public class MobileQuery {
	private int mq_id;
	private String mq_title;
	private String mq_caller;
	private int mq_detno;
	public int getMq_id() {
		return mq_id;
	}
	public void setMq_id(int mq_id) {
		this.mq_id = mq_id;
	}
	public String getMq_title() {
		return mq_title;
	}
	public void setMq_title(String mq_title) {
		this.mq_title = mq_title;
	}
	public String getMq_caller() {
		return mq_caller;
	}
	public void setMq_caller(String mq_caller) {
		this.mq_caller = mq_caller;
	}
	public int getMq_detno() {
		return mq_detno;
	}
	public void setMq_detno(int mq_detno) {
		this.mq_detno = mq_detno;
	}

}
